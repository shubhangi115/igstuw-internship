"""
* inherintance is the oops concept where an object is based on other object
* it is the mechanism of code reuse
* the object that is getting inherited is called superclass and the object that inherits the superclass is called subclass
* instead of starting from a scratch you can create a class by deriving from a pre-existing class by listing the parent class in parentheses after the new class name
* the child class inherits the attributes of its parent class and you can use those attribute as if they were defined in the child class.A child class can also override data members and methods from the parents
syntax:
class Subclassname(parentclass1[,parent])"""